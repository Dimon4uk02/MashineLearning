import numpy as np

# Розмірність першої матриці (2x3)
m1, n1 = 2, 3

# Розмірність другої матриці (3x2)
m2, n2 = 3, 2

# Створюємо першу матрицю з випадковими цілими числами від 1 до 15
matrix1 = np.random.randint(1, 16, (m1, n1))

# Створюємо другу матрицю з випадковими цілими числами від 1 до 15
matrix2 = np.random.randint(1, 16, (m2, n2))

# Виводимо першу матрицю
print("Перша матриця:")
print(matrix1)

# Виводимо другу матрицю
print("Друга матриця:")
print(matrix2)

# Перемножуємо матриці
result = np.dot(matrix1, matrix2)

# Виводимо результат множення
print("Результат множення матриць:")
print(result)
