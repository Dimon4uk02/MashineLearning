import numpy as np

# Розмір матриці
n = 3

# Створюємо матрицю з випадковими дійсними числами в діапазоні від 0 до 1
matrix = np.random.rand(n, n)

# Знаходимо середнє геометричне всіх елементів матриці
geometric_mean = np.prod(matrix) ** (1 / (n * n))

# Виводимо матрицю
print("Матриця:")
print(matrix)

# Виводимо середнє геометричне
print("Середнє геометричне:", geometric_mean)
