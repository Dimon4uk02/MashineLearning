import numpy as np

# Розмір матриці
n = 4

# Створюємо матрицю 4x4 з випадковими дійсними числами в діапазоні від -1 до 1
matrix = np.random.uniform(-1, 1, (n, n))

# Виводимо матрицю
print("Матриця:")
print(matrix)

# Знаходимо вектор-стовпчик кількості від'ємних елементів в кожному рядку матриці
negative_count_vector = np.sum(matrix < 0, axis=1)

# Виводимо вектор-стовпчик
print("Вектор кількості від'ємних елементів в кожному рядку:")
print(negative_count_vector)
